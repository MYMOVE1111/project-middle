#!/usr/bin/env python
import os
import sys
import django

# Setup Django
sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
os.environ.setdefault('DJANGO_SETTINGS_MODULE', 'recipe_sharing.settings')
django.setup()

from recipes.models import Category, Tag

# Категории кухонь мира
categories = [
    ('Итальянская кухня', 'Паста, пицца, ризотто и другие итальянские блюда'),
    ('Японская кухня', 'Суши, рамен, темпура и традиционные японские блюда'),
    ('Мексиканская кухня', 'Тако, буррито, начос и острые мексиканские блюда'),
    ('Французская кухня', 'Круассаны, соусы, десерты и изысканная французская кухня'),
    ('Китайская кухня', 'Дим-сам, лапша, утка по-пекински и другие китайские блюда'),
    ('Индийская кухня', 'Карри, тандури, наан и ароматные индийские специи'),
    ('Грузинская кухня', 'Хачапури, хинкали, шашлык и кавказские блюда'),
    ('Тайская кухня', 'Том ям, пад тай, карри и экзотические тайские блюда'),
    ('Русская кухня', 'Борщ, пельмени, блины и традиционные русские рецепты'),
    ('Американская кухня', 'Бургеры, стейки, барбекю и американский фастфуд'),
    ('Турецкая кухня', 'Кебаб, лахмаджун, баклава и турецкие сладости'),
    ('Корейская кухня', 'Кимчи, бибимбап, корейское барбекю'),
]

for name, desc in categories:
    obj, created = Category.objects.get_or_create(name=name, defaults={'description': desc})
    if created:
        print(f'+ Категория: {name}')

# Теги
tags = [
    'Острое', 'Вегетарианское', 'Веганское', 'Без глютена',
    'Быстрый рецепт', 'Праздничное', 'Детское меню', 'Диетическое',
    'Завтрак', 'Обед', 'Ужин', 'Десерт', 'Закуска', 'Суп',
    'Салат', 'Выпечка', 'Напитки', 'Соусы', 'Гриль', 'На пару',
]

for tag_name in tags:
    obj, created = Tag.objects.get_or_create(name=tag_name)
    if created:
        print(f'+ Тег: {tag_name}')

print(f'\nВсего категорий: {Category.objects.count()}')
print(f'Всего тегов: {Tag.objects.count()}')
